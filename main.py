a, b, c = map(float, input().split())
d = b ** 2 - 4 * a * c
if d > 0:
    x1 = (-b + d ** 0.5) / (2 * a)
    x2 = (-b - d ** 0.5) / (2 * a)
    print(f'Первый корень равный {x1}, Второй корень равный {x2}')
elif d == 0:
    x = (-b) / (2 * a)
    print(f'Два одинаковых корня равных {x}')
else:
    print('Нет корней')